# Direct assignment of integer value to a variable
iVal=10
print(f"Type of the iVal is : {type(iVal)}")
print("")

# Assigning integer value to a variable using input function
jVal=int(input("Enter a Number : "))
print(f"Type of entered value : {type(jVal)}")
print("")

# Integers can grow very large
large_int = 10**1000
print(f"Value of large_int : {large_int}")
print(f"Type of large_int : {type(large_int)}")
